#include <iostream>
using namespace std;

int main()
{
	int MyProject = 100;
	int Open = 10;

	cout << MyProject + Open << endl;
	cout << MyProject - Open << endl;
	cout << MyProject * Open << endl;
	cout << MyProject / Open << endl;
	cout << MyProject % Open << endl;
	

	float B;
	B = 1.5f;

	cout << B + 50 << endl;
	cout << B - 50 << endl;
	cout << B * 50 << endl;
	cout << B / 50 << endl;

	return 0;
}